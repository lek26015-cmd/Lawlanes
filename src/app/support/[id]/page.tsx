
'use client';

import { useState, useEffect, Suspense, useRef } from 'react';
import { useParams, useRouter } from 'next/navigation';
import { getDashboardData } from '@/lib/data';
import type { ReportedTicket } from '@/lib/types';
import { SupportChatBox } from '@/components/chat/support-chat-box';

import {
  Card,
  CardContent,
  CardDescription,
  CardHeader,
  CardTitle,
} from "@/components/ui/card";
import { Button } from '@/components/ui/button';
import { Badge } from '@/components/ui/badge';
import { FileText, Ticket, Upload } from 'lucide-react';
import { format } from 'date-fns';
import { th } from 'date-fns/locale';
import { useToast } from '@/hooks/use-toast';
import { useFirebase } from '@/firebase';

function SupportPageContent() {
    const params = useParams();
    const router = useRouter();
    const ticketId = params.id as string;
    const { firestore, user } = useFirebase();
    
    const [ticket, setTicket] = useState<ReportedTicket | null>(null);
    const [isLoading, setIsLoading] = useState(true);
    const [files, setFiles] = useState<{ name: string, size: number }[]>([]);
    const fileInputRef = useRef<HTMLInputElement>(null);
    const { toast } = useToast();

    useEffect(() => {
        async function fetchTicket() {
            if (!ticketId || !firestore || !user) {
                setIsLoading(false);
                return;
            }
            const { tickets } = await getDashboardData(firestore, user.uid);
            // Simulate changing one ticket to resolved for testing purposes
            const modifiedTickets = tickets.map(t => t.id === 'TICKET-5891A' ? { ...t, status: 'resolved' as const } : t);
            const currentTicket = modifiedTickets.find(t => t.id === ticketId);

            setTicket(currentTicket || null);
            setIsLoading(false);
        }
        fetchTicket();
    }, [ticketId, firestore, user]);

    const handleUploadClick = () => {
        fileInputRef.current?.click();
    };

    const handleFileChange = (event: React.ChangeEvent<HTMLInputElement>) => {
        const file = event.target.files?.[0];
        if (!file) return;

        const MAX_FILE_SIZE = 5 * 1024 * 1024; // 5MB

        if (file.size > MAX_FILE_SIZE) {
            toast({
                variant: "destructive",
                title: "ไฟล์มีขนาดใหญ่เกินไป",
                description: `กรุณาเลือกไฟล์ที่มีขนาดไม่เกิน ${MAX_FILE_SIZE / 1024 / 1024}MB`,
            });
            return;
        }

        setFiles(prevFiles => [...prevFiles, { name: file.name, size: file.size }]);
        toast({
            title: "อัปโหลดไฟล์สำเร็จ (จำลอง)",
            description: `ไฟล์ "${file.name}" ถูกเพิ่มในรายการแล้ว`,
        });

        if(event.target) {
            event.target.value = '';
        }
    };
    
    const statusBadges: { [key: string]: React.ReactNode } = {
        pending: <Badge variant="outline" className="border-yellow-600 text-yellow-700 bg-yellow-50">กำลังตรวจสอบ</Badge>,
        resolved: <Badge variant="secondary" className="bg-green-100 text-green-800 border-green-200">แก้ไขแล้ว</Badge>,
    }

    if (isLoading) {
        return <div>Loading support chat...</div>
    }

    if (!ticket) {
        return <div>Ticket not found.</div>
    }
    
    const isResolved = ticket.status === 'resolved';

    return (
        <div className="container mx-auto px-4 md:px-6 py-8">
            <div className="grid grid-cols-1 lg:grid-cols-3 gap-8">
                <div className="lg:col-span-2">
                    <SupportChatBox ticket={ticket} isDisabled={isResolved} />
                </div>
                <div className="lg:col-span-1 space-y-6">
                    <Card>
                        <CardHeader>
                            <CardTitle className="flex items-center gap-2">
                                <Ticket className="w-5 h-5 text-muted-foreground" />
                                รายละเอียด Ticket
                            </CardTitle>
                        </CardHeader>
                        <CardContent className="space-y-4 text-sm">
                            <div className="flex justify-between">
                                <span className="text-muted-foreground">Ticket ID:</span>
                                <span className="font-mono font-semibold">{ticket.id}</span>
                            </div>
                            <div className="flex justify-between">
                                <span className="text-muted-foreground">เคสที่เกี่ยวข้อง:</span>
                                <span className="font-mono">{ticket.caseId}</span>
                            </div>
                             <div className="flex justify-between">
                                <span className="text-muted-foreground">ประเภทปัญหา:</span>
                                <span className="font-semibold">{ticket.problemType}</span>
                            </div>
                             <div className="flex justify-between">
                                <span className="text-muted-foreground">วันที่แจ้ง:</span>
                                <span>{format(ticket.reportedAt, 'dd MMM yyyy', { locale: th })}</span>
                            </div>
                            <div className="flex justify-between items-center">
                                <span className="text-muted-foreground">สถานะ:</span>
                                {statusBadges[ticket.status]}
                            </div>
                        </CardContent>
                    </Card>

                    <Card>
                        <CardHeader>
                            <CardTitle>เอกสารที่เกี่ยวข้อง</CardTitle>
                        </CardHeader>
                        <CardContent>
                            <div className="space-y-3">
                                <div className="space-y-2 text-sm">
                                    {files.length === 0 ? (
                                        <div className="text-center text-muted-foreground py-4 text-xs">
                                            <FileText className="mx-auto h-8 w-8 mb-2" />
                                            <p>ยังไม่มีเอกสารแนบ</p>
                                        </div>
                                    ) : (
                                        files.map((file, index) => (
                                            <div key={index} className="flex justify-between items-center p-2 rounded-md hover:bg-gray-100">
                                                <div className="flex items-center gap-2 overflow-hidden">
                                                    <FileText className="h-5 w-5 text-muted-foreground flex-shrink-0" />
                                                    <span className="truncate" title={file.name}>{file.name}</span>
                                                </div>
                                                <span className="text-muted-foreground text-xs flex-shrink-0">
                                                    {(file.size / 1024 / 1024).toFixed(2)}MB
                                                </span>
                                            </div>
                                        ))
                                    )}
                                </div>
                                <input 
                                    type="file" 
                                    ref={fileInputRef} 
                                    onChange={handleFileChange}
                                    className="hidden"
                                    disabled={isResolved}
                                />
                                <Button onClick={handleUploadClick} className="w-full" disabled={isResolved}>
                                    <Upload className="mr-2 h-4 w-4" /> อัปโหลดไฟล์
                                </Button>
                            </div>
                        </CardContent>
                    </Card>
                </div>
            </div>
        </div>
    )
}

export default function SupportPage() {
    return (
        <Suspense fallback={<div>Loading...</div>}>
            <SupportPageContent />
        </Suspense>
    )
}
